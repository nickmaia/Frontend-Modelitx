export const createLinearFormulae = (a, b) => {
  return `${a.toFixed(1)}+${b.toFixed(1)}x`;
}

export const createExponencialFormulae = (b, a) => {
  return `${b.toFixed(1)}EXP(${a.toFixed(1)}x)`;
}

export const createSigmoidFormulae = (a, b) => {
  return `${a.toFixed(1)}+${b.toFixed(1)}x`;
}

export const createNormalFormulae = (a, b) => {
  return `${a.toFixed(1)}+${b.toFixed(1)}x`;
}